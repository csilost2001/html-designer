package com.harmony.retail.web;

import jakarta.servlet.http.HttpServletRequest;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.security.web.authentication.logout.SecurityContextLogoutHandler;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.PostMapping;

/**
 * ProcessFlow: 60e08c25-3daa-41b4-a7bd-b8f5fb571349 (ヘッダーガジェット処理)
 * action: act-logout
 * httpRoute: POST /api/retail/auth/logout (auth: required)
 *
 * グローバルヘッダ Gadget (68709449-...) の logoutButton から submit される
 * Form action endpoint。ProcessFlow.httpRoute.path を使うのが FRAGMENT.md 規約。
 *
 * Generated by Harmony /generate-code (techStack.auth.method=session).
 *
 * 注: SecurityConfig の .logout(...) で同 path を登録しているため、Spring Security の
 * LogoutFilter が先にセッション破棄を行う。本 Controller の実装は Filter が無効化された
 * ケース (テスト等) のためのフォールバック。
 */
@Controller
public class AuthController {

    /**
     * act-logout: ログアウト処理
     * - SecurityContextLogoutHandler でセッション破棄 + SecurityContext クリア
     * - 完了後は /login?logout に redirect
     */
    @PostMapping("/api/retail/auth/logout")
    public String logout(HttpServletRequest request) {
        Authentication auth = SecurityContextHolder.getContext().getAuthentication();
        new SecurityContextLogoutHandler().logout(request, null, auth);
        return "redirect:/login?logout";
    }
}
