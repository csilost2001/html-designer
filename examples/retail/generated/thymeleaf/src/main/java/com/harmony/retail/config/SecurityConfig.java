package com.harmony.retail.config;

import org.springframework.boot.autoconfigure.security.servlet.PathRequest;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.Profile;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.core.userdetails.User;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.crypto.factory.PasswordEncoderFactories;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.security.provisioning.InMemoryUserDetailsManager;
import org.springframework.security.web.SecurityFilterChain;
import org.springframework.security.web.util.matcher.AntPathRequestMatcher;

/**
 * Spring Security 設定 — techStack.auth.method=session 時に生成
 *
 * - form login enabled (/login)
 * - POST /api/retail/auth/logout を logout endpoint として登録 (ProcessFlow の act-logout から派生)
 * - logout 後の redirect 先: /login?logout
 * - 静的リソース (/css/**, /js/**) は anonymous 許可
 *
 * 注: 本デモは InMemoryUserDetailsManager で固定ユーザー (demo / demo) を持つ。
 * プロジェクト要件に応じて DB 連携 (JdbcUserDetailsManager) や OAuth2 / LDAP 等に
 * 差し替えてください。
 *
 * Fix in #1037: Spring Security 6 で requestMatchers("/css/**") は DispatcherServlet 経由の
 * リクエストのみマッチする。PathRequest.toStaticResources().atCommonLocations() を追加することで
 * Spring Boot の ResourceHttpRequestHandler が提供する静的リソースパスも許可する。
 * 将来 #1036 系列で /generate-code skill template にも反映候補。
 *
 * Generated by Harmony /generate-code (techStack.auth.method=session, #1035 D).
 */
@Configuration
@EnableWebSecurity
public class SecurityConfig {

    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        http
            .authorizeHttpRequests(auth -> auth
                // Spring Boot 静的リソース (ResourceHttpRequestHandler) を anonymous 許可
                .requestMatchers(PathRequest.toStaticResources().atCommonLocations()).permitAll()
                .requestMatchers("/login", "/css/**", "/js/**", "/webjars/**").permitAll()
                .anyRequest().authenticated()
            )
            .formLogin(form -> form
                .loginPage("/login").permitAll()
                .defaultSuccessUrl("/", true)
            )
            .logout(logout -> logout
                .logoutRequestMatcher(new AntPathRequestMatcher("/api/retail/auth/logout", "POST"))
                .logoutSuccessUrl("/login?logout")
                .invalidateHttpSession(true)
                .deleteCookies("JSESSIONID")
            );
        return http.build();
    }

    @Bean
    public PasswordEncoder passwordEncoder() {
        return PasswordEncoderFactories.createDelegatingPasswordEncoder();
    }

    /**
     * デモ用 InMemoryUserDetailsManager — **dev profile 限定** (#1035 S-6 解消)。
     *
     * 本番では `@Profile("prod")` 用に JdbcUserDetailsManager / OAuth2 / LDAP 等の
     * 別 UserDetailsService Bean を提供すること。dev 以外で起動すると Spring Security 6 は
     * NoSuchBeanDefinitionException で起動失敗するため、本番 profile 用の Bean 提供が必須。
     */
    @Bean
    @Profile("dev")
    public UserDetailsService demoUserDetailsService(PasswordEncoder passwordEncoder) {
        UserDetails demoUser = User.builder()
            .username("demo")
            .password(passwordEncoder.encode("demo"))
            .roles("USER")
            .build();
        return new InMemoryUserDetailsManager(demoUser);
    }
}
